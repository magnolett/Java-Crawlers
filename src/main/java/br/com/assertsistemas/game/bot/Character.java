package br.com.assertsistemas.game.bot;

public class Character {
	
	String nameChar = "";
	String vocChar = "";
	String levelChar = "";
	String residenceChar = "";
	String guildChar = "";
	String isOnline = "";
	
	public String getNameChar() {
		return nameChar;
	}
	public void setNameChar(String nameChar) {
		this.nameChar = nameChar;
	}
	public String getVocChar() {
		return vocChar;
	}
	public void setVocChar(String vocChar) {
		this.vocChar = vocChar;
	}
	public String getLevelChar() {
		return levelChar;
	}
	public void setLevelChar(String levelChar) {
		this.levelChar = levelChar;
	}
	public String getResidenceChar() {
		return residenceChar;
	}
	public void setResidenceChar(String residenceChar) {
		this.residenceChar = residenceChar;
	}
	public String getGuildChar() {
		return guildChar;
	}
	public void setGuildChar(String guildChar) {
		this.guildChar = guildChar;
	}
	public String getIsOnline() {
		return isOnline;
	}
	public void setIsOnline(String isOnline) {
		this.isOnline = isOnline;
	}
	
	

}
